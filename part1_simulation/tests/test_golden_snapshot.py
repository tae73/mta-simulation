# ============================================================
# 2. summary_stats.json — conversion rate, skew, segment ordering
# ============================================================

def test_summary_conversion_rate_in_window():
    """Realized conversion rate lands in the calibrated [1.5%, 4%] band."""
    ss = _load("summary_stats.json")
    cr = ss["conversion_rate"]
    assert 0.015 <= cr <= 0.04, f"conversion_rate out of band: {cr}"


def test_summary_conversion_rate_self_consistent():
    """conversion_rate == n_converted / n_users (internal consistency)."""
    ss = _load("summary_stats.json")
    np.testing.assert_allclose(
        ss["conversion_rate"], ss["n_converted"] / ss["n_users"], rtol=1e-9
    )


def test_summary_journey_length_right_skewed():
    """Journey-length distribution is strongly right-skewed (skew > 1.5)."""
    ss = _load("summary_stats.json")
    assert ss["journey_length_skew"] > 1.5, (
        f"journey_length_skew not right-skewed: {ss['journey_length_skew']}"
    )


def test_summary_journey_length_mean_exceeds_median():
    """Right-skew corollary: mean journey length > median."""
    ss = _load("summary_stats.json")
    assert ss["journey_length_mean"] > ss["journey_length_median"]


def test_summary_segment_conversion_ordering():
    """Per-segment conversion ordering: Exploratory > Loyal > New."""
    ss = _load("summary_stats.json")
    by_seg = ss["conversion_rate_by_segment"]
    assert by_seg["Exploratory"] > by_seg["Loyal"] > by_seg["New"], (
        f"segment conversion ordering violated: {by_seg}"
    )


def test_summary_segment_counts_cover_all_segments():
    """Segment counts span the 3 canonical segments and sum to n_users."""
    ss = _load("summary_stats.json")
    counts = ss["segment_counts"]
    assert set(counts) == set(SEGMENT_NAMES)
    assert sum(counts.values()) == ss["n_users"]


def test_summary_segment_proportions_match_config():
    """Segment shares match the 0.5 / 0.3 / 0.2 DGP proportions (sampling slack)."""
    ss = _load("summary_stats.json")
    counts = ss["segment_counts"]
    n = ss["n_users"]
    np.testing.assert_allclose(counts["New"] / n, 0.5, atol=0.02)
    np.testing.assert_allclose(counts["Exploratory"] / n, 0.3, atol=0.02)
    np.testing.assert_allclose(counts["Loyal"] / n, 0.2, atol=0.02)


def test_summary_no_timestamp_violations():
    """DGP must emit chronologically valid journeys (0 timestamp violations)."""
    ss = _load("summary_stats.json")
    assert ss["timestamp_violations"] == 0


