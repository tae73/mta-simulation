# 3. cost_summary.json — Paid Search dominates total spend
# ============================================================

def test_cost_paid_search_dominates_total_spend():
    """Paid Search (CPC $2.50) is >= 95% of total cost."""
    cs = _load("cost_summary.json")
    share = cs["channel_total_cost"]["Paid Search"] / cs["total_spend"]
    assert share >= 0.95, f"Paid Search cost share too low: {share}"


def test_cost_zero_cost_channels_are_free():
    """Organic Search, Referral, Direct are zero-cost observation channels."""
    cs = _load("cost_summary.json")
    for ch in ("Organic Search", "Referral", "Direct"):
        np.testing.assert_allclose(
            cs["channel_total_cost"][ch], 0.0, atol=1e-9
        )


def test_cost_total_spend_equals_channel_sum():
    """total_spend == Σ channel_total_cost (internal consistency)."""
    cs = _load("cost_summary.json")
    np.testing.assert_allclose(
        cs["total_spend"], sum(cs["channel_total_cost"].values()), rtol=1e-9
    )


def test_cost_per_conversion_consistent():
    """cost_per_conversion == total_spend / n_converters."""
    cs = _load("cost_summary.json")
    np.testing.assert_allclose(
        cs["cost_per_conversion"],
        cs["total_spend"] / cs["n_converters"],
        rtol=1e-9,
    )


def test_cost_avg_per_touchpoint_matches_config_order():
    """Avg cost/touchpoint ranks Paid Search (CPC) far above CPM/fixed channels."""
    cs = _load("cost_summary.json")
    avg = cs["channel_avg_cost_per_touchpoint"]
    assert avg["Paid Search"] > avg["Social"] > avg["Display"] > avg["Email"] > 0.0


def test_cost_summary_matches_embedded_copy():
    """summary_stats.json embeds an identical copy of cost_summary.json."""
    cs = _load("cost_summary.json")
    embedded = _load("summary_stats.json")["cost_summary"]
    assert cs == embedded, "cost_summary.json drifted from summary_stats embed"
